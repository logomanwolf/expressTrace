package po;

public class NoRepeatPath {
	private String fromP;
	private String endP;
	private Integer id;
	public String getFromP() {
		return fromP;
	}

	public void setFromP(String fromP) {
		this.fromP = fromP;
	}

	public String getEndP() {
		return endP;
	}

	public void setEndP(String endP) {
		this.endP = endP;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

}
